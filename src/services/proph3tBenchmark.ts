export interface ReportNarrative {
  cover: { subtitle: string };
  executiveSummary: ExecutiveSummary;
  sectionInsights: Record<SectionId, SectionInsight>;
  source: 'llm' | 'deterministic' | 'mixed';
}

export async function produceFullReportNarrative(
  banks: Bank[],
  rubrics: RubricRef[],
  context?: { profile?: ClientProfile; focusBankId?: string },
): Promise<ReportNarrative> {
  const sections: SectionId[] = ['benchmark', 'evolution', 'zone', 'drift', 'compliance', 'basket', 'forecast'];

  const exec = await generateExecutiveSummary(banks, rubrics);
  const sectionResults = await Promise.all(
    sections.map((s) => analyzeBenchmarkSection(s, banks, rubrics, context)),
  );

  const sectionInsights: Record<string, SectionInsight> = {};
  sections.forEach((s, i) => {
    sectionInsights[s] = sectionResults[i];
  });

  const llmCount = [exec.source === 'llm', ...sectionResults.map((r) => r.source === 'llm')].filter(Boolean).length;
  const source: ReportNarrative['source'] =
    llmCount === sections.length + 1 ? 'llm'
    : llmCount === 0 ? 'deterministic'
    : 'mixed';

  return {
    cover: {
      subtitle: exec.headline,
    },
    executiveSummary: exec,
    sectionInsights: sectionInsights as Record<SectionId, SectionInsight>,
    source,
  };
}

