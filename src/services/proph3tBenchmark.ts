export type SectionId = 'benchmark' | 'evolution' | 'zone' | 'drift' | 'compliance' | 'basket' | 'forecast';

export interface SectionInsight {
  /** 2–4 paragraphs of expert commentary */
  paragraphs: string[];
  /** Numbered list of concrete actions */
  actions: string[];
  source: 'llm' | 'deterministic';
}

export async function analyzeBenchmarkSection(
  section: SectionId,
  banks: Bank[],
  rubrics: RubricRef[],
  context?: { profile?: ClientProfile; focusBankId?: string },
): Promise<SectionInsight> {
  const det = computeSectionInsightDeterministic(section, banks, rubrics, context);
  if (!isLlmAvailable()) return { ...det, source: 'deterministic' };

  const result = await llmCall(
    SYSTEM_ANALYST + ' Réponds en JSON {"paragraphs": string[], "actions": string[]}.',
    buildSectionPrompt(section, banks, rubrics, det, context),
    { role: 'fast', temperature: 0.25, maxTokens: 900, jsonMode: true },
  );
  if (!result) return { ...det, source: 'deterministic' };

  try {
    const parsed = JSON.parse(result.content);
    return {
      paragraphs: Array.isArray(parsed.paragraphs) ? parsed.paragraphs.map(String) : det.paragraphs,
      actions: Array.isArray(parsed.actions) ? parsed.actions.map(String) : det.actions,
      source: 'llm',
    };
  } catch {
    return { ...det, source: 'deterministic' };
  }
}

function computeSectionInsightDeterministic(
  section: SectionId,
  banks: Bank[],
  rubrics: RubricRef[],
  context?: { profile?: ClientProfile; focusBankId?: string },
): Omit<SectionInsight, 'source'> {
  switch (section) {
    case 'benchmark': {
      const agg = computeAggressiveness(banks, rubrics);
      const top = agg.slice(0, 3);
      return {
        paragraphs: [
          `La cohorte rassemble ${banks.length} banques sur ${rubrics.length} rubriques tarifaires. Le profil d'agressivité — calculé comme le pourcentage de rubriques où la banque dépasse le 3e quartile — situe ${top[0]?.bankName ?? '—'} en tête (${((top[0]?.aboveQ3Pct ?? 0) * 100).toFixed(0)} %).`,
          `À l'opposé du spectre, ${agg[agg.length - 1]?.bankName ?? '—'} se positionne sous le 1er quartile sur ${((agg[agg.length - 1]?.belowQ1Pct ?? 0) * 100).toFixed(0)} % des rubriques observées, ce qui en fait un benchmark de référence pour challenger les autres établissements.`,
        ],
        actions: top.slice(0, 3).map((a, i) => `${i + 1}. Documenter la position tarifaire de ${a.bankName} (score ${a.score.toFixed(2)}) dans les revues de portefeuille client.`),
      };
    }
    case 'evolution': {
      const focusBank = banks.find((b) => b.id === context?.focusBankId) ?? banks[0];
      const grids = focusBank ? focusBank.conditionGrids ?? [] : [];
      return {
        paragraphs: [
          `${focusBank?.name ?? 'Cette banque'} a publié ${grids.length} version${grids.length > 1 ? 's' : ''} de grille tarifaire. L'analyse chronologique permet de distinguer les rubriques structurellement stables de celles soumises à une dérive haussière régulière.`,
          `Une variation supérieure à 10 % entre deux versions consécutives mérite d'être interrogée : alignement réglementaire ? Ajustement à l'inflation ? Refonte commerciale ? Le détail par rubrique permet de qualifier la nature du mouvement.`,
        ],
        actions: [
          '1. Recouper chaque variation >10 % avec la communication officielle de la banque.',
          '2. Identifier les rubriques qui dérivent simultanément (signal d\'une refonte tarifaire globale).',
          '3. Notifier les clients impactés par les hausses les plus significatives.',
        ],
      };
    }
    case 'zone': {
      return {
        paragraphs: [
          `La parité fixe entre XAF et XOF rend les comparaisons CEMAC ↔ UEMOA directement exploitables sur les frais en monnaie. Les écarts de médiane traduisent des positionnements concurrentiels et réglementaires distincts entre les deux zones.`,
          `Un écart >10 % sur une rubrique standardisée (taux usure, tenue de compte) suggère soit une différence de cadre réglementaire (BCEAO vs COBAC), soit une intensité concurrentielle hétérogène. Les agios et taux d'usure sont les rubriques les plus sensibles à différencier.`,
        ],
        actions: [
          '1. Identifier les rubriques où l\'écart inter-zones dépasse 15 % (signal d\'arbitrage).',
          '2. Confronter ces écarts au texte réglementaire pour distinguer ce qui relève du cadre L1 versus du marché.',
          '3. Construire un argumentaire d\'opportunité pour les clients pouvant déplacer leur activité entre zones.',
        ],
      };
    }
    case 'drift': {
      return {
        paragraphs: [
          `Les dérives tarifaires sont le signal faible d'un changement de stratégie commerciale. Une banque qui augmente brutalement de >30 % sur une rubrique a soit alignement réglementaire, soit recherche de marge, soit transfert de coût d'une rubrique à une autre.`,
          `Les dérives critiques (>100 %) dénotent un repositionnement majeur — souvent l'apparition d'un nouveau service ou la suppression d'une gratuité historique. Elles méritent une investigation systématique.`,
        ],
        actions: [
          '1. Pour chaque dérive critique, vérifier que la banque a notifié ses clients dans le délai réglementaire (préavis de 60 jours en UEMOA).',
          '2. Cartographier les dérives par catégorie pour détecter les déplacements de marge (baisse sur cartes → hausse sur agios).',
          '3. Constituer une note de sensibilisation client pour les dérives haussières >30 %.',
        ],
      };
    }
    case 'compliance': {
      const reps = checkCohortCompliance(banks);
      const totalCritical = reps.reduce((s, r) => s + r.criticalCount, 0);
      return {
        paragraphs: [
          `Le contrôle de conformité confronte chaque grille active aux plafonds BCEAO et COBAC d'ordre public. La cohorte présente ${totalCritical} dépassement(s) critique(s).`,
          `Un dépassement critique (taux d'usure, plafond CCM) constitue une infraction au cadre L1 : il ouvre droit à restitution pour les clients concernés et peut faire l'objet d'une saisine du régulateur.`,
        ],
        actions: reps
          .filter((r) => r.criticalCount > 0)
          .slice(0, 3)
          .map(
            (r, i) =>
              `${i + 1}. ${r.bankName} : constituer un dossier de restitution sur les ${r.criticalCount} infraction(s) critique(s) — voir références BCEAO/COBAC dans la liste détaillée.`,
          ),
      };
    }
    case 'basket': {
      const profile = context?.profile ?? 'particulier_basique';
      const baskets = computeCostBasket(banks, profile);
      const cheapest = baskets[0];
      const dearest = baskets[baskets.length - 1];
      const spread = cheapest && dearest ? dearest.totalAnnual - cheapest.totalAnnual : 0;
      return {
        paragraphs: [
          `Le panier ${profile} génère un coût annuel allant de ${cheapest?.totalAnnual.toLocaleString('fr-FR') ?? '—'} XAF (${cheapest?.bankName ?? '—'}) à ${dearest?.totalAnnual.toLocaleString('fr-FR') ?? '—'} XAF (${dearest?.bankName ?? '—'}). L'écart absolu est de ${spread.toLocaleString('fr-FR')} XAF/an.`,
          `Cette comparaison ne reflète qu'un profil-type. Le coût réel pour un client donné dépend de son volume de transactions effectif. Néanmoins, l'écart factuel matérialise l'enjeu de la négociation tarifaire.`,
        ],
        actions: [
          `1. Présenter ce panier-type au client lors des revues annuelles de relation bancaire.`,
          `2. Adapter le panier au profil réel du client (volumes de virements, retraits, etc.).`,
          `3. Utiliser ${cheapest?.bankName ?? 'la banque la moins chère'} comme référence dans les négociations.`,
        ],
      };
    }
    case 'forecast': {
      const focusBank = banks.find((b) => b.id === context?.focusBankId) ?? banks[0];
      const forecasts = focusBank ? forecastBank(focusBank, rubrics) : [];
      const rising = forecasts.filter((f) => f.cagrPct != null && f.cagrPct > 5).slice(0, 3);
      return {
        paragraphs: [
          `${focusBank?.name ?? 'Cette banque'} présente ${forecasts.length} rubriques avec assez d'historique pour une projection. ${rising.length} rubrique(s) montrent un taux de croissance annuel composé supérieur à 5 %.`,
          `Les projections sont des extrapolations linéaires : elles supposent la continuité du pattern observé. Tout choc réglementaire ou stratégique invaliderait ces estimations. À utiliser comme signal de surveillance, pas comme prévision contractuelle.`,
        ],
        actions: rising.map(
          (f, i) =>
            `${i + 1}. Surveiller ${f.rubricLabel} — projection à 12 mois : ${f.forecast12m?.toLocaleString('fr-FR') ?? '—'} (CAGR ${f.cagrPct?.toFixed(1) ?? '—'} %).`,
        ),
      };
    }
  }
}

function buildSectionPrompt(
  section: SectionId,
  banks: Bank[],
  rubrics: RubricRef[],
  det: Omit<SectionInsight, 'source'>,
  context?: { profile?: ClientProfile; focusBankId?: string },
): string {
  return `Section : ${section}.
Périmètre : ${banks.length} banques, ${rubrics.length} rubriques.
${context?.focusBankId ? `Banque focus : ${banks.find((b) => b.id === context.focusBankId)?.name ?? 'inconnue'}.` : ''}
${context?.profile ? `Profil client : ${context.profile}.` : ''}

Synthèse déterministe (référence) :
${det.paragraphs.map((p, i) => `(${i + 1}) ${p}`).join('\n')}

Actions déterministes :
${det.actions.join('\n')}

Tâche : reformule cette analyse pour qu'elle soit plus incisive et nuancée. 2 à 4 paragraphes courts, 3 actions numérotées. Garde les chiffres exacts. Pas de superlatifs.`;
}

