/** Simplified report-level summary of the cohort compliance, useful for the PDF. */
export function summarizeCompliance(banks: Bank[]): {
  overallScore: number;
  total: number;
  perfect: number;
  withCritical: number;
  worstReport: ComplianceReport | null;
} {
  const reps = checkCohortCompliance(banks);
  const overall = reps.length === 0 ? 100 : reps.reduce((s, r) => s + r.score, 0) / reps.length;
  return {
    overallScore: Math.round(overall),
    total: reps.length,
    perfect: reps.filter((r) => r.score === 100).length,
    withCritical: reps.filter((r) => r.criticalCount > 0).length,
    worstReport: reps[0] ?? null,
  };
}
