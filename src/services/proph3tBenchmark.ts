export interface NlqAnswer {
  answer: string;
  source: 'llm' | 'deterministic';
  /** Citations / data points used */
  citations: string[];
}

export async function answerNaturalLanguageQ(
  question: string,
  banks: Bank[],
  rubrics: RubricRef[],
): Promise<NlqAnswer> {
  // Build a compact data digest the LLM can cite from
  const digest = buildDataDigest(banks, rubrics);

  if (!isLlmAvailable()) {
    return {
      answer:
        'Le moteur d\'analyse Proph3t n\'est pas connecté. Active une instance Ollama dans Paramètres → Intelligence pour activer les requêtes en langage naturel. ' +
        'En attendant, utilise les sections Benchmark, Évolution, CEMAC vs UEMOA, Conformité et Dérives pour explorer la donnée.',
      source: 'deterministic',
      citations: [],
    };
  }

  const result = await llmCall(
    SYSTEM_ANALYST + ' Tu réponds aux questions sur les données du benchmark fournies. Cite toujours les chiffres exacts. Si la donnée manque, dis-le explicitement plutôt que de spéculer.',
    `${digest}\n\nQuestion : ${question}\n\nRéponds en français, 4 lignes maximum, en citant les chiffres pertinents.`,
    { role: 'fast', temperature: 0.2, maxTokens: 600 },
  );

  if (!result) {
    return {
      answer: 'Le moteur Proph3t a échoué à répondre. Réessaye dans un instant ou consulte les sections détaillées du module.',
      source: 'deterministic',
      citations: [],
    };
  }

  return {
    answer: result.content.trim(),
    source: 'llm',
    citations: digest.split('\n').filter((l) => l.startsWith('•')),
  };
}

function buildDataDigest(banks: Bank[], rubrics: RubricRef[]): string {
  const lines: string[] = ['== Digest du benchmark ==', `${banks.length} banques, ${rubrics.length} rubriques.`];
  for (const b of banks) {
    const grid = getActiveGrid(b);
    if (!grid) continue;
    lines.push(`• ${b.name} (${b.code}, ${getZone(b) ?? 'zone inconnue'}) — grille ${grid.name} (effective ${formatDate(grid.effectiveDate)}).`);
    for (const r of rubrics.slice(0, 12)) {
      const v = getNumericValue(grid, r.path);
      if (v == null || v === 0) continue;
      lines.push(`    - ${r.label} : ${v} ${r.unit}`);
    }
  }
  // Add aggregates
  const agg = computeAggressiveness(banks, rubrics).slice(0, 3);
  lines.push('== Top 3 banques agressives ==');
  agg.forEach((a) => {
    lines.push(`• ${a.bankName} — score ${a.score.toFixed(2)}, Q3+ sur ${(a.aboveQ3Pct * 100).toFixed(0)} %.`);
  });
  return lines.join('\n');
}

function formatDate(d: Date | string): string {
  const dt = typeof d === 'string' ? new Date(d) : d;
  return dt.toLocaleDateString('fr-FR');
}

