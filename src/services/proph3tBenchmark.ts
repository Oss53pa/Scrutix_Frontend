export interface ExecutiveSummary {
  /** 1-line headline (auditor's verdict) */
  headline: string;
  /** Bullet narrative — 5 to 8 items */
  bullets: string[];
  /** Top 3 actionable recommendations */
  recommendations: string[];
  /** Provenance: 'llm' = generated by Proph3t, 'deterministic' = computed locally */
  source: 'llm' | 'deterministic';
}

const SYSTEM_ANALYST = `Tu es un auditeur senior spécialisé en tarification bancaire CEMAC/UEMOA, intervenant pour le compte d'AtlasBanx. Tu rédiges des analyses concises, factuelles, en français, sans superlatifs ni langage commercial. Tu cites toujours les chiffres exacts. Tes conclusions sont actionnables.`;

export async function generateExecutiveSummary(
  banks: Bank[],
  rubrics: RubricRef[],
): Promise<ExecutiveSummary> {
  // Always compute the deterministic version first — used as fallback and
  // also as the data scaffold the LLM commentates on.
  const det = computeDeterministicSummary(banks, rubrics);

  if (!isLlmAvailable()) {
    return { ...det, source: 'deterministic' };
  }

  const userPrompt = buildUserPrompt(banks, rubrics, det);
  const result = await llmCallJson<{
    headline: string;
    bullets: string[];
    recommendations: string[];
  }>(
    SYSTEM_ANALYST + ' Réponds UNIQUEMENT en JSON valide avec les clés "headline" (string), "bullets" (string[]) et "recommendations" (string[]).',
    userPrompt,
    ['headline', 'bullets', 'recommendations'],
    { role: 'fast', temperature: 0.2, maxTokens: 1200 },
  );

  if (!result) return { ...det, source: 'deterministic' };

  return {
    headline: String(result.data.headline || det.headline).slice(0, 240),
    bullets: (Array.isArray(result.data.bullets) ? result.data.bullets : det.bullets)
      .map(String)
      .filter(Boolean)
      .slice(0, 8),
    recommendations: (Array.isArray(result.data.recommendations)
      ? result.data.recommendations
      : det.recommendations)
      .map(String)
      .filter(Boolean)
      .slice(0, 5),
    source: 'llm',
  };
}

function computeDeterministicSummary(
  banks: Bank[],
  rubrics: RubricRef[],
): Omit<ExecutiveSummary, 'source'> {
  const cov = computeCoverage(banks, rubrics);
  const agg = computeAggressiveness(banks, rubrics);
  const compliance = checkCohortCompliance(banks);
  const totalGrids = banks.reduce((s, b) => s + (b.conditionGrids?.length ?? 0), 0);
  const banksWithGrid = banks.filter((b) => (b.conditionGrids?.length ?? 0) > 0).length;

  const mostAggressive = agg.length > 0 ? agg[0] : null;
  const mostCompetitive = agg.length > 0 ? agg[agg.length - 1] : null;
  const worstCompliance = compliance.length > 0 ? compliance[0] : null;

  const bullets: string[] = [];
  bullets.push(
    `Périmètre : ${banksWithGrid}/${banks.length} banques avec grille active, ${totalGrids} version(s) de grille au total, ${rubrics.length} rubriques suivies.`,
  );
  bullets.push(
    `Couverture documentaire : ${(cov.stats.coverageRate * 100).toFixed(0)} % des cellules (banque × rubrique) renseignées.`,
  );
  if (mostAggressive && mostAggressive.n > 0) {
    bullets.push(
      `${mostAggressive.bankName} affiche le profil tarifaire le plus agressif : ${(mostAggressive.aboveQ3Pct * 100).toFixed(0)} % de ses rubriques sont au-dessus du Q3 du marché (échantillon n=${mostAggressive.n}).`,
    );
  }
  if (mostCompetitive && mostCompetitive.n > 0 && mostCompetitive.score < 0) {
    bullets.push(
      `${mostCompetitive.bankName} est globalement en-dessous du marché : ${(mostCompetitive.belowQ1Pct * 100).toFixed(0)} % de ses rubriques sous le Q1.`,
    );
  }
  if (worstCompliance && worstCompliance.violationsCount > 0) {
    bullets.push(
      `Conformité réglementaire : ${worstCompliance.bankName} cumule ${worstCompliance.violationsCount} dépassement(s) (${worstCompliance.criticalCount} critique(s), ${worstCompliance.highCount} sévère(s)) — score ${worstCompliance.score}/100.`,
    );
  }

  // Drift narrative
  const totalDrifts = banks.reduce((sum, b) => {
    const grids = [...(b.conditionGrids ?? [])].sort(
      (a, c) => new Date(a.effectiveDate).getTime() - new Date(c.effectiveDate).getTime(),
    );
    let count = 0;
    for (let i = 1; i < grids.length; i++) {
      for (const r of rubrics) {
        const a = getNumericValue(grids[i - 1], r.path);
        const c = getNumericValue(grids[i], r.path);
        if (a == null || c == null || a === 0) continue;
        if (Math.abs((c - a) / a) >= 0.1) count++;
      }
    }
    return sum + count;
  }, 0);
  if (totalDrifts > 0) {
    bullets.push(
      `Dérives tarifaires détectées : ${totalDrifts} variations >10 % entre versions consécutives sur l'ensemble des grilles.`,
    );
  }

  const recommendations: string[] = [];
  if (mostAggressive && mostAggressive.score > 0.3) {
    recommendations.push(
      `Mettre en surveillance les contrats clients sous ${mostAggressive.bankName} — exposition tarifaire significative (>${(mostAggressive.aboveQ3Pct * 100).toFixed(0)} % de rubriques en haut de marché).`,
    );
  }
  if (worstCompliance && worstCompliance.criticalCount > 0) {
    recommendations.push(
      `Saisir le régulateur : ${worstCompliance.bankName} présente ${worstCompliance.criticalCount} dépassement(s) critique(s) du cadre BCEAO/COBAC. Constituer un dossier de remboursement.`,
    );
  }
  if (cov.stats.coverageRate < 0.6) {
    recommendations.push(
      `Lancer une campagne de relance documentaire — la couverture du benchmark (${(cov.stats.coverageRate * 100).toFixed(0)} %) reste insuffisante pour des conclusions robustes sur certaines rubriques.`,
    );
  }
  if (mostCompetitive && mostCompetitive.score < -0.2) {
    recommendations.push(
      `Utiliser la grille ${mostCompetitive.bankName} comme levier de négociation : son positionnement compétitif sert de référence pour challenger les autres établissements.`,
    );
  }

  let headline = `Audit tarifaire de ${banksWithGrid} banque${banksWithGrid > 1 ? 's' : ''} — ${rubrics.length} rubriques observées.`;
  if (worstCompliance && worstCompliance.criticalCount > 0) {
    headline = `Alerte conformité : ${worstCompliance.criticalCount} dépassement(s) critique(s) BCEAO/COBAC détecté(s).`;
  } else if (totalDrifts > 5) {
    headline = `Marché tarifaire en mouvement : ${totalDrifts} dérives >10 % observées sur la cohorte.`;
  } else if (mostAggressive && mostAggressive.score > 0.4) {
    headline = `${mostAggressive.bankName} se distingue comme l'acteur le plus cher de l'échantillon.`;
  }

  return {
    headline,
    bullets,
    recommendations,
  };
}

function buildUserPrompt(
  banks: Bank[],
  rubrics: RubricRef[],
  det: Omit<ExecutiveSummary, 'source'>,
): string {
  const cov = computeCoverage(banks, rubrics);
  const agg = computeAggressiveness(banks, rubrics).slice(0, 5);
  const compliance = checkCohortCompliance(banks).slice(0, 5);

  // Sample distributions for a few key rubriques
  const keyRubrics = rubrics
    .filter((r) =>
      [
        'accountFees.tenueCompte.particulier',
        'creditFees.tauxDecouvertAutorise',
        'creditFees.tauxUsureLegal',
        'cardFees.visaClassic',
      ].includes(r.path),
    );
  const distSnippets = keyRubrics
    .map((r) => {
      const samples: BankRubricValue[] = [];
      for (const b of banks) {
        const grid = getActiveGrid(b);
        if (!grid) continue;
        const v = getNumericValue(grid, r.path);
        if (v == null || v === 0) continue;
        samples.push({
          bankId: b.id,
          bankCode: b.code,
          bankName: b.name,
          zone: getZone(b),
          value: v,
        });
      }
      const dist = computeDistribution(samples.map((s) => s.value));
      if (!dist) return null;
      return `- ${r.label} (${r.unit}) — n=${dist.n}, médiane=${dist.median.toFixed(2)}, Q1=${dist.q1.toFixed(2)}, Q3=${dist.q3.toFixed(2)}, min=${dist.min.toFixed(2)}, max=${dist.max.toFixed(2)}`;
    })
    .filter(Boolean)
    .join('\n');

  return `Données du benchmark tarifaire :

== Périmètre ==
${banks.length} banques, ${rubrics.length} rubriques, couverture ${(cov.stats.coverageRate * 100).toFixed(0)} %.

== Profil d'agressivité (top 5) ==
${agg
  .map(
    (a, i) =>
      `${i + 1}. ${a.bankName} — score ${a.score.toFixed(2)} (au-dessus du Q3 sur ${(a.aboveQ3Pct * 100).toFixed(0)} % des rubriques, en-dessous du Q1 sur ${(a.belowQ1Pct * 100).toFixed(0)} %).`,
  )
  .join('\n')}

== Conformité BCEAO/COBAC (top 5 worst) ==
${compliance
  .map(
    (c, i) =>
      `${i + 1}. ${c.bankName} — score ${c.score}/100 — ${c.violationsCount} violation(s) (${c.criticalCount} critique(s), ${c.highCount} sévère(s)).`,
  )
  .join('\n')}

== Distributions clés ==
${distSnippets || '(données insuffisantes)'}

== Synthèse déterministe (référence) ==
Headline : ${det.headline}
${det.bullets.map((b) => '• ' + b).join('\n')}

Tâche : produit une synthèse exécutive structurée pour ce benchmark. La synthèse doit être incisive, factuelle, et orientée action pour un cabinet d'audit. 5 à 7 bullets maximum, 3 recommandations actionnables. Utilise des chiffres exacts.`;
}

